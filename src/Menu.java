
public class Menu {
    private static String Dishes;

    public static String getDishes(int dishes) {
        if (dishes == 1) {
            return Dishes = "dishes 1:";
        } else if (dishes == 2) {
            return Dishes = "dishes 2:";
        } else if (dishes == 3) {
            return Dishes = "dishes 3:";
        } else if (dishes == 4) {
            return Dishes = "dishes 4:";
        } else if (dishes == 5) {
            return Dishes = "dishes 5:";
        } else if (dishes == 6) {
            return Dishes = "dishes 6:";
        } else {
            return Dishes = "";
        }
    }
}
