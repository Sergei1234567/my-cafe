public class FreeTables {
    private int table;
    private int numberOfSeats;

    public FreeTables(int table, int numberOfSeats) {
        this.table = table;
        this.numberOfSeats = numberOfSeats;
    }

    public int getNumberOfSeats() {
        return numberOfSeats;
    }

    public int getTable() {
        return table;
    }
}
